@implementation Favorites

@synthesize managedObjectContext, favoritesArray;

#pragma mark -
#pragma mark View lifecycle


- (void)viewDidLoad {
    [super viewDidLoad];

	// Create a Fetch Request
	NSFetchRequest *request = [[NSFetchRequest alloc] init];
	NSEntityDescription *entity = [NSEntityDescription entityForName:@"Item" inManagedObjectContext:managedObjectContext];
	[request setEntity:entity];
	
	
	// Set up sorting behavior	
	NSSortDescriptor *sortDescriptor = [[NSSortDescriptor alloc] initWithKey:@"course" ascending:NO];
	NSArray *sortDescriptors = [[NSArray alloc] initWithObjects:sortDescriptor, nil];
	[request setSortDescriptors:sortDescriptors];
	[sortDescriptors release];
	[sortDescriptor release];
	
	
	// Execute the Requests and store locally
	NSError *error;
	NSMutableArray *mutableFetchResults = [[managedObjectContext executeFetchRequest:request error:&error] mutableCopy];
	if (mutableFetchResults == nil) {
		// Handle the error
	}
	
	// Sets our local copy
	[self setFavoritesArray:mutableFetchResults];
	[mutableFetchResults release];
	[request release];
	
}


